void drv_lcdDrawPicture(const UINT8 *ptr)
{
    UINT8 i, j;
    
    ptr += 1023;
    CS0 = 0;
    for(i = 0; i < 8; i++)
    {
        drv_lcdWriteCmd(0xb0 + i);
        drv_lcdWriteCmd(0x10);
        drv_lcdWriteCmd(0x00);
        for(j = 0; j < 128; j++)
        {
            drv_lcdWriteData(swapTable[*ptr--]);
        }
    }
}
