//--------------------------------------------------------- 4线SPI接口 驱动

#define    CS0		PD_ODR_ODR2
#define    RSTB		PB_ODR_ODR0
#define    RS		PD_ODR_ODR3
#define    SCL		PD_ODR_ODR4
#define    SDI		PD_ODR_ODR7


static UINT8 PicBuff[128 * 8] = {0};


void drv_lcdWriteCmd(UINT8 cmd);
void drv_lcdWriteData(UINT8 data);

void drv_lcdReset(void)
{
    RSTB = 0;
    sys_delayms(2);
    RSTB = 1;
}

void drv_lcdInit(void)
{
    drv_lcdReset();
    sys_delayms(10);

    drv_lcdWriteCmd(0xe2);		//Soft rest

    drv_lcdWriteCmd(0xa2);		//SET LCD bias(A2-1/9bias; A3-1/7bias)
    drv_lcdWriteCmd(0xa0);		//SET ADC NORMAL(OB-POR seg0-00h) A0: NORMAL  A1: REVERSE
    drv_lcdWriteCmd(0xc8);		//SET COM OUTPUT SCAN DIRECTION(0XXXB-NORMAL)-POR  COM63-->COM0

    drv_lcdWriteCmd(0xa4);		// SET DISPLAY NORMAL (0B-NORMAL)-POR  A4:NORMAL A5:ENTIRE DISPLAY ON
    drv_lcdWriteCmd(0xa6);		//SET NORMAL DISPLAY MODE(0B-NORMAL)  A6:NORMAL  A7:REVERSE
    drv_lcdWriteCmd(0x25);		//SET INTERNAL REGULATOR RESDASTOR RATIO(100B)-POR

    drv_lcdWriteCmd(0x81);

    drv_lcdWriteCmd(0x0f);		// SET CONTRAST CONTROL REGISTER(00,0000H-11,1111H) 30  对比度请修改此值，调浓增大此值，反之调淡。
    drv_lcdWriteCmd(0x2f);		//SET POWER CONTROL REGISTER (ALL INTERNAL)

    drv_lcdWriteCmd(0x40);		//end of initialzation

    drv_lcdDrawPicture(PicBuff);
    
    drv_lcdWriteCmd(0xaf);		//DisPlay On
}

void drv_lcdWriteCmd(UINT8 cmd)
{
    UINT8 i;
    RS = 0;
    CS0 = 0;
    for(i = 0; i < 8; i++)
    {
        SCL = 0;
        if(cmd & 0x80)
        {
            SDI = 1;
        }
        else
        {
            SDI = 0;
        }
        SCL = 1;
        cmd = (cmd << 1);

    }
}

void drv_lcdWriteData(UINT8 data)
{
    UINT8 i;
    RS = 1;
    CS0 = 0;

    for(i = 0; i < 8; i++)
    {
        SCL = 0;
        if(data & 0x80)
        {
            SDI = 1;
        }
        else
        {
            SDI = 0;
        }
        SCL = 1;
        data = (data << 1);
    }
}

void drv_lcdDrawFillColor(UINT8 color)
{
    UINT16 i;
    UINT8 data;
    if(color)
    {
        data = 0xFF;
    }
    else
    {
        data = 0x00;
    }
    for(i = 0; i < 1024; i++)
    {
        PicBuff[i] = data;
    }
}

void Ldrv_lcdDisPlayPicBuff(void)
{
    drv_lcdDrawPicture(PicBuff);
}

void drv_lcdDrawPoint(UINT8 x, UINT8 y, UINT8 color)
{
    UINT8 pic_bit;
    UINT16 pic_byte;
    x &= 0x7F;
    y &= 0x3F;
    pic_bit = y & 0x07;
    pic_byte = ((y >> 3) << 7) | x;

    if(color == 0x01)
        PicBuff[pic_byte] |= 1 << pic_bit;
    else if(color == 0x00)
        PicBuff[pic_byte] &= ~(1 << pic_bit);
    else
        PicBuff[pic_byte] ^= 1 << pic_bit;
}

UINT8 drv_lcdReadPoint(UINT8 x, UINT8 y)
{
    UINT8 pic_bit;
    UINT16 pic_byte;
    x &= 0x7F;
    y &= 0x3F;
    pic_bit = y & 0x07;
    pic_byte = ((y >> 3) << 7) | x;

    if(PicBuff[pic_byte] & ( 1 << pic_bit))
    {
        return 1;
    }
    else
    {
        return 0;
    }
}

